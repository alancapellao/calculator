    public void arithmetic_operation() {

        switch (calculation) {

            case 1:
                ans = num + Double.parseDouble(txtTela.getText());
                txtTela.setText("" + Double.toString(ans));
                break;

            case 2:
                ans = num - Double.parseDouble(txtTela.getText());
                txtTela.setText("" + Double.toString(ans));
                break;

            case 3:
                ans = num / Double.parseDouble(txtTela.getText());
                txtTela.setText("" + Double.toString(ans));
                break;

            case 4:
                ans = num * Double.parseDouble(txtTela.getText());
                txtTela.setText("" + Double.toString(ans));
                break;
        }
    }

