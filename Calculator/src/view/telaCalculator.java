    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed

        int length = txtTela.getText().length();
        int number = txtTela.getText().length() - 1;
        String store;

        if (length > 0) {

            StringBuilder back = new StringBuilder(txtTela.getText());
            back.deleteCharAt(number);
            store = back.toString();
            txtTela.setText(store);
        }
    }//GEN-LAST:event_btnDelActionPerformed

